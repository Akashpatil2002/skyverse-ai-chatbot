const userSchema = new mongoose.Schema({

  name: {
    type: String,
    required: true,
  },

  email: {
    type: String,
    required: true,
    unique: true,
  },

  password: {
    type: String,
    required: function () {
      return this.provider !== "google";
    },
  },

  provider: {
    type: String,
    default: "local",
  },

  profilePic: {
    type: String,
  },

});

module.exports = mongoose.model("User", userSchema);
